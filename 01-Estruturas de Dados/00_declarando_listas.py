#####################
# Declarando Listas #
#####################

#Declarando strings dentro de uma lista e exibindo
frutas = ["maçã", "banana", "pera", "laranja"]
print(frutas)

#Lista Vazia
frutas = []
print(frutas)

#Função list() -> Separa os caracteres por vírgulas 
letras = list("python")
print(letras)

#Função list(range()) -> Conta de acordo com o valor informado
numeros = list(range(10))
print(numeros)

#Declaração de variáveis diferentes em uma lista
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)