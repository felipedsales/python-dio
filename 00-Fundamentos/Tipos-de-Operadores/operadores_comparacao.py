################################
#_# Operadores de Comparação #_#
################################

n1 = 10
n2 = 12

# Operador Igual ==
print(n1 == n2)

# Operador Diferente !=
print(n1 != n2)

# Operador Maior >
print(n1 > n2)

# Operador Igual Maior Igual >=
print(n1 >= n2)

# Operador Igual Menor <
print(n1 < n2)

# Operador Igual Menor Igual <=
print(n1 <= n2)