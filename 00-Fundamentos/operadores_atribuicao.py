############################
# Operadores de Atribuição #
############################

n1 = 100
print(n1)

# Adição
n1 += 10
print(n1)

#Subtração
n1 -= 10
print(n1)

#Divisão Inteira
n1 //= 3
print(n1)

#Divisão
n1 /= 2
print(n1)

#Multiplicação
n1 *= 3
print(n1)

#Modulação
n1 %= 2
print(n1)

#Exponenciação
n1 **= 2
print(n1)